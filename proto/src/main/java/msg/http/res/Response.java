package msg.http.res;

public class Response {
	private int ret;
	private String msg;

	public Response() {
	}

	public int getRet() {
		return this.ret;
	}

	public void setRet(int ret) {
		this.ret = ret;
	}

	public String getMsg() {
		return this.msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
